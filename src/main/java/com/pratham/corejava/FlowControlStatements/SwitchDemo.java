package com.pratham.corejava.FlowControlStatements;

public class SwitchDemo {
    public static void main(String[] args) {
        int x=10;

        switch (x) {
            case 1:
                System.out.println("case 1");
                break;
            case 2:
                System.out.println("Case 2");
                break;
            default:
                System.out.println("Default");
        }
    }
}
